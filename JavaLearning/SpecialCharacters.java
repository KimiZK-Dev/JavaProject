package JavaLearning;

public class SpecialCharacters {
    public static void main(String[] args) {
        System.out.println("Line1\nLine2");       // \n - Xuống dòng mới
        System.out.println("Hello\rWorld");       // \r - Đưa con trỏ về đầu dòng
        System.out.println("Column1\tColumn2");   // \t - Tab
        System.out.println("ABC\bD");             // \b - Xoá 1 ký tự phía trước
        System.out.println("First\fSecond");      // \f - Form Feed (hiệu ứng tùy hệ thống, thường khó thấy rõ)
    
    }
}
